// Reverse lookup: 5-digit FIPS → canonical display name (Title-Cased).
// Built once at module load. Used when ZIP→county lookup returns a FIPS code
// and we need to render its name.
//
// Picks the FIRST entry seen per FIPS, since the source-file convention is
// to write the canonical name first followed by aliases:
//   'MIAMI-DADE':'12086', 'MIAMI DADE':'12086', 'DADE':'12086'
//   ↑ canonical          ↑ alias              ↑ alias
const COUNTY_NAME_BY_FIPS = (function() {
  const out = {};
  for (const stateFips of Object.keys(COUNTY_FIPS)) {
    for (const upperName of Object.keys(COUNTY_FIPS[stateFips])) {
      const fips = COUNTY_FIPS[stateFips][upperName];
      if (out[fips]) continue;  // first-seen wins (canonical, by convention)
      // Title-case for display: 'MIAMI-DADE' → 'Miami-Dade', 'ST. LUCIE' → 'St. Lucie'
      out[fips] = upperName.toLowerCase()
        .split(/\s+/).map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ')
        .split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join('-');
    }
  }
  return out;
})();

